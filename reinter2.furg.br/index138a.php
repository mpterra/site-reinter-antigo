<h2>A oferta é destinada a professores (as), estudantes, técnicos (as) e pesquisadores (as) da FURG e outras instituições associadas ao GCUB.</h2>
</div>
<div>
<div>
<p>O Grupo de Cooperação Internacional de Universidades Brasileiras (GCUB), em parceria com a Educational Testing Service (ETS), abriu nova oportunidade para solicitação de voucher de <strong>desconto de 15% </strong>para o<strong> Exame TOEFL iBT</strong>. </p>
<p>O valor em dólares da prova com o desconto de 15% é de <strong>US$ 182,75 </strong>dólares americanos. O interessado deverá depositar ou transferir para a conta do GCUB o valor do exame em reais, calculado em função do câmbio do dia. A conversão deverá ser realizada por meio do site oficial do <a href="https://www.bcb.gov.br/conversao.">Banco Central</a>.</p>
<p>O interessado deverá preencher o formulário <a href="https://form.jotform.com/223043503039647">disponível aqui</a>, após o pagamento da taxa. O prazo para a transferência e preenchimento do formulário se encerra às 23h59min do dia<strong> 28 de fevereiro de 2023.</strong></p>
<p>Os vouchers serão emitidos pela ETS e enviados pelo GCUB aos interessados no prazo de até 15 dias úteis. Após seu recebimento, o beneficiário terá o prazo de <strong>até um ano </strong>para realizar o Exame TOEFL iBT.</p>
<p>Em caso de necessidade de esclarecimentos adicionais, entre em contato por meio do endereço eletrônico <strong>assistente.gcub@gmail.com.</strong></p>
<p><strong>*Lembramos que essa oportunidade possui caráter independente, ou seja, a REINTER apoia a divulgação, mas não se responsabiliza pelo processo de inscrição.</strong></p>
