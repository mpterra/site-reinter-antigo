<ol>
<li>
<p><strong>Hospedagem:</strong> Os estudantes ficarão hospedados em casas de famílias anfitriãs da comunidade universitária da Universidad Politécnica de Tulancingo (UPT).</p>
</li>
<li>
<p><strong>Alimentação:</strong> A universidade de destino arcará com as três refeições diárias do estudante. Durante os dias úteis, o café da manhã e o almoço serão servidos na cafeteria da UPT, enquanto o jantar será na casa da família anfitriã. Aos fins de semana, todas as refeições serão providenciadas pela família anfitriã.</p>
</li>
<li>
<p><strong>Isenção de taxas universitárias:</strong> Os estudantes não precisarão se preocupar com custos de matrícula ou taxas acadêmicas durante o período de intercâmbio.</p>
</li>
</ol>
