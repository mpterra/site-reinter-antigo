			<title>FURG lança Edital PILA 2024 para Intercâmbio Acadêmico</title>
			<link>https://reinter2.furg.br/index.php?option=com_content&amp;view=article&amp;id=987:furg-lanca-edital-pila-2024-para-intercambio-academico&amp;catid=18&amp;Itemid=137</link>
			<guid isPermaLink="true">https://reinter2.furg.br/index.php?option=com_content&amp;view=article&amp;id=987:furg-lanca-edital-pila-2024-para-intercambio-academico&amp;catid=18&amp;Itemid=137</guid>
			<description><![CDATA[<p><strong>Inscrições até 30 de abril</strong></p>
<hr />
<p>A Universidade Federal do Rio Grande (FURG), através da Secretaria de Relações Internacionais (REINTER), anuncia a abertura das inscrições para o Programa de Intercâmbio Acadêmico Latino-Americano (PILA). Este programa visa enriquecer a formação acadêmica, profissional e cultural dos estudantes, além de promover a internacionalização do ensino superior.</p>
<p><strong>Detalhes do Programa:</strong></p>
<ul>
<li><strong>Objetivo:</strong> Ampliar a experiência internacional dos estudantes, reforçando os laços de cooperação entre universidades da América Latina.</li>
<li><strong>Público Alvo:</strong> Estudantes regularmente matriculados em cursos de graduação da FURG.</li>
<li><strong>Vagas:</strong> Duas vagas disponíveis para o segundo semestre de 2024.
<ul>
<li><strong>Universidades Destino:</strong>
<ul>
<li>Universidad Nacional de Rafaela</li>
<li>Universidade de Santo Tomás - Villavicencio</li>
</ul>
</li>
</ul>
</li>
</ul>
<p><strong>Condições de Participação:</strong></p>
<ul>
<li>Os candidatos devem ser maiores de 18 anos e ter até 30 anos ao final do intercâmbio.</li>
<li>Necessário estar matriculado na FURG durante o processo de seleção e realização do intercâmbio.</li>
<li>Coeficiente acadêmico mínimo exigido é 7,0.</li>
<li>O estudante não pode ter mais que duas reprovações acumuladas e deve ter integralizado ao menos 50% do curso.</li>
</ul>
<p><strong>Benefícios:</strong></p>
<ul>
<li>Isenção de taxas acadêmicas na universidade de destino.</li>
<li>Alojamento e alimentação fornecidos pela instituição anfitriã.</li>
</ul>
<p><strong>Processo de Inscrição:</strong></p>
<ul>
<li><strong>Período de Inscrição:</strong> De 15 a 30 de abril de 2024.</li>
<li><strong>Como se Inscrever:</strong> Através do formulário disponível no link: <a href="https://forms.gle/Gdb9f1t7P3zPg4kk8" target="_new">Formulário de Inscrição</a>.</li>
</ul>
<p><strong>Documentação Necessária:</strong></p>
<ul>
<li>Formulário de Inscrição Preenchido.</li>
<li>Histórico escolar atualizado.</li>
<li>Currículo Lattes documentado.</li>
<li>Cópia do passaporte ou protocolo de solicitação.</li>
<li>Certificado de proficiência em espanhol (se necessário).</li>
<li>Carta de motivação em espanhol.</li>
</ul>
<p><strong>Critérios de Avaliação:</strong></p>
<ul>
<li>Documentação (peso 4), Coeficiente de Rendimento (peso 4) e Carta de Motivação (peso 2).</li>
<li>Confira o Anexo 1, <a href="https://reinter2.furg.br/images/ANEXO_1_PILA_2024.pdf" target="_blank" rel="noopener noreferrer">clicando aqui. </a></li>
</ul>
<p><strong>Divulgação dos Resultados:</strong></p>
<ul>
<li>Os resultados serão publicados até o dia 10 de maio de 2024 no site da Reinter.</li>
</ul>
<p><strong>Responsabilidades do Estudante:</strong></p>
<ul>
<li>Custos com visto, seguro internacional, e despesas pessoais não cobertas pelo programa são de responsabilidade do estudante.</li>
</ul>
<p>Esta é uma excelente oportunidade para os estudantes da FURG que desejam expandir seus horizontes acadêmicos e experienciar novas culturas. As inscrições são limitadas e os interessados devem verificar todos os requisitos e condições no <a href="https://reinter2.furg.br/images/EDITAL_PILA.pdf" target="_blank" rel="noopener noreferrer">edital completo.</a></p>]]></description>
