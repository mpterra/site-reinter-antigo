			<description><![CDATA[<h2 data-sourcepos="1:1-1:87"> </h2>
<p data-sourcepos="3:1-3:294">A Universidade Federal do Rio Grande (FURG) está com inscrições abertas para o <strong>Programa ESCALA</strong>, que oferece <strong>bolsas de estudo para intercâmbio</strong> em universidades da <strong>América Latina</strong> para <strong>estudantes de graduação</strong>. As inscrições podem ser feitas de <strong>10 de abril a 13 de maio de 2024</strong>.</p>
<div class="page" title="Page 1">
<div class="layoutArea">
<div class="column">
<p>O objetivo da chamada é incentivar a mobilidade acadêmica dos estudantes de graduação da Universidade Federal do Rio Grande – FURG em outras universidades, de forma a contribuir e enriquecer com a formação acadêmica e pessoal.</p>
</div>
</div>
</div>
<p data-sourcepos="5:1-5:29"><strong>O que o programa oferece:</strong></p>
<ul data-sourcepos="7:1-11:0">
<li data-sourcepos="7:1-7:61"><strong>Isenção de taxas acadêmicas</strong> na universidade de destino;</li>
<li data-sourcepos="8:1-8:73"><strong>Auxílio financeiro para cobrir custos de vida</strong> durante a mobilidade;</li>
<li data-sourcepos="9:1-9:85"><strong>Oportunidade de estudar em uma nova cultura</strong> e conhecer diferentes perspectivas;</li>
<li data-sourcepos="10:1-11:0"><strong>Enriquecer o currículo</strong> e aumentar as chances de empregabilidade.</li>
</ul>
<p data-sourcepos="12:1-12:28"><strong>Quem pode se candidatar:</strong></p>
<ul data-sourcepos="14:1-21:0">
<li data-sourcepos="14:1-14:74"><strong>Estudantes regularmente matriculados em cursos de graduação da FURG</strong>;</li>
<li data-sourcepos="15:1-15:36">Com <strong>coeficiente mínimo de 7,0</strong>;</li>
<li data-sourcepos="16:1-16:37">Ter <strong>no máximo duas reprovações</strong>;</li>
<li data-sourcepos="17:1-17:47">Ter <strong>integralizado no mínimo 40% do curso</strong>;</li>
<li data-sourcepos="18:1-18:47">Não estar em <strong>semestre de colação de grau</strong>;</li>
<li data-sourcepos="19:1-19:31">Não ocupar <strong>cargo docente</strong>;</li>
<li data-sourcepos="20:1-21:0">Ter <strong>conhecimento básico de língua espanhola</strong> (algumas universidades exigem certificado específico).</li>
</ul>
<p data-sourcepos="22:1-22:22"><strong>Vagas disponíveis:</strong></p>
<ul data-sourcepos="24:1-25:0">
<li data-sourcepos="24:1-25:0"><strong>8 vagas</strong> para universidades da Argentina, Bolívia, Paraguai e Uruguai.</li>
</ul>
<p data-sourcepos="26:1-26:22"><strong>Como se inscrever:</strong></p>
<ol data-sourcepos="28:1-36:0">
<li data-sourcepos="28:1-28:118">Acesse o formulário de inscrição online: <a class="traceable-link" href="https://forms.gle/43k1jikXtHo6c9xPA" target="_blank" rel="noopener noreferrer">https://forms.gle/43k1jikXtHo6c9xPA</a></li>
<li data-sourcepos="29:1-29:62">Preencha o formulário com seus dados pessoais e acadêmicos;</li>
<li data-sourcepos="30:1-36:0">Anexe os documentos exigidos:
<ul data-sourcepos="31:5-36:0">
<li data-sourcepos="31:5-31:24">Histórico Escolar;</li>
<li data-sourcepos="32:5-32:35">Currículo Lattes documentado;</li>
<li data-sourcepos="33:5-33:56">Cópia do passaporte (ou protocolo de agendamento);</li>
<li data-sourcepos="34:5-34:37">Carta de motivação em espanhol;</li>
<li data-sourcepos="35:5-36:0">Certificado de proficiência em língua espanhola (ou autodeclaração).</li>
</ul>
</li>
</ol>
