			<description><![CDATA[<p>A Secretaria de Relações Internacionais (REINTER) da FURG torna pública a abertura de inscrições para a seleção de acadêmicos de graduação para o acesso ao Programa de mobilidade ESCALA Graduação da Associação de Universidades Grupo Montevidéu (AUGM) para o semestre letivo de 2025/1. </p>
<p>O objetivo do Programa é incentivar a mobilidade acadêmica dos estudantes de graduação da FURG em outras universidades estrangeiras pertencentes à AUGM, de forma a contribuir e enriquecer com a formação acadêmica e pessoal.</p>
<p>Conforme edital, <strong>serão ofertadas três (03) vagas</strong> em universidades da AUGM. A classificação final será composta por apenas 01 (um/uma) aprovado(a) e até 02 (dois) suplentes para cada vaga. </p>
<p>A mobilidade compreende<strong> o período de um semestre letivo</strong>, relativo ao primeiro semestre de 2025, podendo ser estendido para mais um semestre acadêmico se acordado entre as instituições.</p>
<p><strong>INSCRIÇÕES</strong> </p>
<p>As inscrições serão realizadas de <b>23 de setembro a 13 de outubro de 2024</b><strong> </strong>, por meio do formulário Google, <a href="https://docs.google.com/forms/d/e/1FAIpQLSdCMYTXW8geXgqq-wnFZyh31-4ulMS2MY3qJGLIZKDJcXjGbQ/viewform?pli=1">disponível aqui.</a></p>
