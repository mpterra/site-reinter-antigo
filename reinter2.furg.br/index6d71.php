<h3><strong>Detalhes do programa BRAMEX:</strong></h3>
<ul>
<li><strong>Objetivo:</strong> Fomentar o intercâmbio de estudantes de graduação entre a FURG e universidades mexicanas conveniadas.</li>
<li><strong>Universidade Conveniada:</strong> Universidad Politécnica de Tulancingo <a href="http://www.upt.edu.mx/" target="_new">Mais informações</a></li>
