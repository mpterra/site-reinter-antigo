<p><strong>Inscrições abertas até 24 de abril para o programa Study Abroad #DestinUEx</strong></p>
<p>A Universidade de Extremadura, localizada na Espanha, está promovendo uma oportunidade única de intercâmbio acadêmico para estudantes internacionais através do programa Study Abroad #DestinUEx. O programa foi desenvolvido para atender às necessidades de formação de estudantes que buscam aprimorar suas habilidades acadêmicas em uma universidade com forte vocação internacional.</p>
<p>O Study Abroad #DestinUEx oferece um esquema de estudo flexível e personalizado, incluindo um serviço completo de acolhimento e acompanhamento aos estudantes. Além de auxiliar na escolha e elaboração do plano de estudo, a Universidade de Extremadura acompanhará os alunos durante toda a mobilidade, permitindo que se concentrem em sua formação.</p>
<p>Os estudantes podem escolher entre <strong>diferentes modalidades</strong>, como estudos parciais<strong> de graduação ou mestrado</strong>, conclusão de um Mestrado Oficial válido em toda a Europa, programas de formação online ou cursos próprios. A universidade oferece orientação para que os alunos possam projetar o programa mais adequado aos seus interesses</p>
<p>Alunos matriculados em universidades parceiras da Universidade de Extremadura, como a FURG, podem participar do programa Study Abroad sem custo adicional para realizar estudos parciais de graduação por um semestre. <b>Estudantes da FURG estão isentos das taxas universitárias, porém, devem arcar com os custos de viagem, hospedagem e alimentação.</b></p>
<p><b>A #DestinUEx de Estudo no Exterior inclui:</b></p>
<ul>
<li>Orientação antes, durante e depois da estadia.</li>
<li>Acesso direto aos estudos selecionados*</li>
<li>Informações e conselhos sobre questões extra-acadêmicas:</li>
<li>Organização da viagem</li>
<li>Obtenção de um visto</li>
<li>Contratação de seguro de saúde</li>
<li>Gestão de alojamentos.</li>
<li>Assessoria acadêmica.</li>
<li>Acesso <a href="https://www.unex.es/organizacion/servicios-universitarios/"><b>às instalações e serviços da UEx.</b></a></li>
<li>Obtenção do Mestrado Oficial (em caso de estudos completos) válido no Espaço Europeu do Ensino Superior.</li>
<li>Estágios em empresas/organizações que irão melhorar a sua empregabilidade.</li>
</ul>
<p>Os interessados em se candidatar devem encaminhar os seguintes documentos para o endereço eletrônico reinter.mobilidade@furg.br até às 12h do dia 24 de abril.</p>
<p><strong>Envie a seguinte documentação:</strong></p>
<ul>
<li>Nome completo;</li>
<li>Data de Nascimento</li>
<li>E-mail;</li>
<li>Número de Whatsapp com DDD;</li>
<li>Número de matrícula ou SIAPE;</li>
<li>Informar a ocupação, semestre e curso(estudante de graduação, pós-graduação, mestrado ou doutorado).</li>
<li>Cópia do seu passaporte ou documento de identidade;</li>
<li>Documento comprovativo de que está a fazer estudos universitários (certificado académico, carta de admissão, etc.) ou que cumpre os requisitos de acesso à universidade;</li>
<li>Certificado oficial de língua espanhola que ateste um mínimo de nível B2 ou intermediário</li>
</ul>
